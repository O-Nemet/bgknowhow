                <li><strong>Morgl the Oracle</strong> (Tier 1, Murloc) - The first time this is Passed each turn, gain +3/+3.</li>
                <li><strong>Fortune-Hunter Flo</strong> (Tier 2, Neutral) - Battlecry: Your teammate gets a Gold Coin.</li>
